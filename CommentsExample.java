public class CommentsExample {
    public static void main(String[] args) {
        System.out.println("Hello, World");
        int x = 10,y=2;
        System.out.println("Value of a: " +(x-y));
        System.out.println("Value of b: " +(x+y));
        System.out.println("Value of c: " +(x%y));
    }
}
